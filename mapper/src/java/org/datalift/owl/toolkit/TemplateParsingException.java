package org.datalift.owl.toolkit;

@SuppressWarnings("serial")
public class TemplateParsingException extends Exception {

	public TemplateParsingException(String msg) {
		super(msg);
	}

	public TemplateParsingException(Throwable cause) {
		super(cause);
	}

	public TemplateParsingException(String msg, Throwable cause) {
		super(msg, cause);
	}
	
}
